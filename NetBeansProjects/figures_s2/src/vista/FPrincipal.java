    public Punt getPuntXY() {
        return new Punt(Integer.parseInt(tfX.getText()),
                Integer.parseInt(tfY.getText()));
    }

    public Cercle getCercle() {
        Cercle c = null;
        try {
            c = new Cercle(Double.parseDouble(tfRadi.getText()));
        } catch (NumberFormatException ex) {
            throw new NumberFormatException("El valor ha de ser un double");
        }
        try {
            c.setPosicio(new Punt(Integer.parseInt(tfX.getText()),
                    Integer.parseInt(tfY.getText())));
        } catch (NumberFormatException ex) {
            throw new NumberFormatException("El valor ha de ser un enter");
        }
        return c;
    }

    public Figura getQuadrat() {
        return new Quadrat(Double.parseDouble(tfAltura.getText()),
                Integer.parseInt(tfX.getText()),
                Integer.parseInt(tfY.getText()));
    }

    public Figura getRectangle() {
        return new Rectangle(Double.parseDouble(tfAltura.getText()),
                Double.parseDouble(tfAmplada.getText()),
                Integer.parseInt(tfX.getText()),
                Integer.parseInt(tfY.getText()));
    }

